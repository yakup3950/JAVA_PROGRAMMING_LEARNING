package m10_variables_practice;

public class CharVariable {
    public static void main(String[] args) {
        char ch1 = 'a';
        System.out.println(ch1);
        char ch2 = 65;
        System.out.println(ch2);
        char ch3 = 0x61;
        System.out.println(ch3);
        char ch4 = 2316;
        System.out.println(ch4);
    }
}
