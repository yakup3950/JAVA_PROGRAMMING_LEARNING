package m10_variables_practice;

/* Practice-1 Declare variables for each primitive data type of whole numbers, assign possible value and print them
    Byte(-128/127) - Short - Int - Long */

public class Variables {

    public static void main(String[] args) {

        byte mark = -128;
        short a = -32_768;
        int b = 2_000_000_000;
        long d = 1_000_000_000_000_000_000L;

        System.out.println(mark);
        System.out.println(a);
        System.out.println(b);
        System.out.println(d);
    }
}
