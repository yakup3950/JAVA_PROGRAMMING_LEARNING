package m10_variables_practice;

public class FloatingPoints {
    public static void main(String[] args) {

        float maxFloat = 3.14F;
        double maxDouble1 = 3.14D;
        double maxDouble2 = 3.14F;
        System.out.println(maxFloat);
        System.out.println(maxDouble1);
        System.out.println(maxDouble2);
    }
}
